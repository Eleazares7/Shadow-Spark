function createObjectsFromArray(array) {
    const objects = [];

    for (let i = 0; i < array.length; i++) {
        if (array[i] != 0) {
            objects.push(
                new CollisionBlock({
                    position: {
                        x: (i % 39) * 36,
                        y: Math.floor(i / 39) * 36,
                    },
                })
            );
        }
    }

    return objects;
}
